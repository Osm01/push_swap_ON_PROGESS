#include "push_swap.h"

void	swap(t_stack **stack)
{
	int	swap_element;

	if (!stack || !*stack)
		return ;
	swap_element = (*stack)->value;
	(*stack)->value = (*stack)->next->value;
	(*stack)->next->value = swap_element;
}

void	sa(t_stack **stack_a)
{
	swap(stack_a);
	ft_printf("sa\n");
}

void	sb(t_stack **stack_b)
{
	swap(stack_b);
	ft_printf("sb\n");
}
